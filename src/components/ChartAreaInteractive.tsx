const chartConfig = {
  desktop: { label: "Desktop", color: "#3b82f6" },
  mobile: { label: "Mobile", color: "#10b981" },
}

export function ChartAreaInteractive() {
  const [timeRange, setTimeRange] = React.useState("90d")
  const [selectedKeys, setSelectedKeys] = React.useState(["desktop", "mobile"])
  const [loadedKeys, setLoadedKeys] = React.useState(["desktop", "mobile"])
  const [hoverData, setHoverData] = React.useState(null)

  const filteredData = chartData.filter((item) => {
    const date = new Date(item.date)
    const referenceDate = new Date("2024-06-30")
    let daysToSubtract = 90
    if (timeRange === "30d") daysToSubtract = 30
    else if (timeRange === "7d") daysToSubtract = 7
    const startDate = new Date(referenceDate)
    startDate.setDate(startDate.getDate() - daysToSubtract)
    return date >= startDate
  })

  return (
    <Card className="pt-0">
      <CardHeader className="flex items-center gap-2 space-y-0 border-b py-1 sm:flex-row px-2">
        <div className="grid flex-1 gap-1">
          <CardTitle className="text-lg">Area Chart - Interactive</CardTitle>
        </div>
        <Select value={timeRange} onValueChange={setTimeRange}>
          <SelectTrigger className="hidden w-[160px] rounded-lg sm:ml-auto sm:flex">
            <SelectValue placeholder="Last 3 months" />
          </SelectTrigger>
          <SelectContent className="rounded-xl bg-background">
            <SelectItem value="90d">Last 3 months</SelectItem>
            <SelectItem value="30d">Last 30 days</SelectItem>
            <SelectItem value="7d">Last 7 days</SelectItem>
          </SelectContent>
        </Select>
      </CardHeader>

      <CardContent className="grid grid-cols-4 gap-2 p-0">
        <ChartContainer config={chartConfig} className="aspect-auto min-h-[250px] w-full col-span-3 p-2">
          <AreaChart
            data={filteredData}
            onMouseMove={({ activePayload }) =>
              setHoverData(activePayload?.[0]?.payload || null)
            }
            onMouseLeave={() => setHoverData(null)}
          >
            <defs>
              <linearGradient id="fillDesktop" x1="0" y1="0" x2="0" y2="1">
                <stop offset="5%" stopColor="#3b82f6" stopOpacity={0.8} />
                <stop offset="95%" stopColor="#3b82f6" stopOpacity={0.1} />
              </linearGradient>
              <linearGradient id="fillMobile" x1="0" y1="0" x2="0" y2="1">
                <stop offset="5%" stopColor="#10b981" stopOpacity={0.8} />
                <stop offset="95%" stopColor="#10b981" stopOpacity={0.1} />
              </linearGradient>
            </defs>
            <CartesianGrid vertical={false} />
            <XAxis
              dataKey="date"
              tickLine={false}
              axisLine={false}
              tickMargin={8}
              minTickGap={32}
              tickFormatter={(value) =>
                new Date(value).toLocaleDateString("en-US", {
                  month: "short",
                  day: "numeric",
                })
              }
            />
            {loadedKeys.includes("desktop") && (
              <Area dataKey="desktop" type="natural" fill="url(#fillDesktop)" stroke="#3b82f6" stackId="a" />
            )}
            {loadedKeys.includes("mobile") && (
              <Area dataKey="mobile" type="natural" fill="url(#fillMobile)" stroke="#10b981" stackId="a" />
            )}
          </AreaChart>
        </ChartContainer>

        <div className="col-span-1 flex flex-col justify-between border-l p-2">
          <div className="space-y-4">
            <div className="text-sm text-muted-foreground text-left font-medium">
              {hoverData?.date
                ? new Date(hoverData.date).toLocaleDateString("en-US", {
                    month: "short",
                    day: "numeric",
                  })
                : "timestamp"}
            </div>

            <div>
              {Object.entries(chartConfig).map(([key, { label, color }]) => {
                const checked = selectedKeys.includes(key)
                const isLoaded = loadedKeys.includes(key)
                const value = hoverData?.[key]
                return (
                  <label key={key} className="flex items-center justify-between transition-colors p-1 rounded">
                    <div className="flex items-center space-x-2">
                      <Checkbox
                        checked={checked}
                        onCheckedChange={(checked) => {
                          setSelectedKeys((prev) =>
                            checked ? [...prev, key] : prev.filter((k) => k !== key)
                          )
                        }}
                      />
                      <span className="font-normal text-sm lowercase" style={{ color }}>{label}</span>
                    </div>
                    {checked && isLoaded && hoverData && (
                      <span className="text-xs font-normal text-muted-foreground">{value}</span>
                    )}
                  </label>
                )
              })}
            </div>
          </div>

          <button
            onClick={() => setLoadedKeys([...selectedKeys])}
            disabled={JSON.stringify(loadedKeys) === JSON.stringify(selectedKeys)}
            className="mt-4 w-full rounded bg-primary text-white px-4 py-2 hover:bg-primary/90 disabled:opacity-50 disabled:cursor-not-allowed"
          >
            Load
          </button>
        </div>
      </CardContent>
    </Card>
  )
}
